    private void selectAnimal() {

        Integer fila = table1.getSelectedRow();

        if (fila >= 0) {

            animal_detalles.cargarPorId(table1.getValueAt(fila, 0).toString());
            btn_detalles.setEnabled(true);
        } else {

            btn_detalles.setEnabled(false);
        }
    }

    private void cargarTablaMuertes() {

        cargarMuertes(table1, tipo, fecha_ini, fecha_fin, animal);

        btn_Reporte.setEnabled(false);

        if (table1.getRowCount() > 0) {
            btn_Reporte.setEnabled(true);
        }
    }

    public void cargarSalidas() {

        cargarMovimientosSalida(table1, tipo, fecha_ini, fecha_fin, animal);
        
        btn_Reporte.setEnabled(false);

        if (table1.getRowCount() > 0) {
            btn_Reporte.setEnabled(true);
        }
    }

