    private void btn_detallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_detallesActionPerformed

        if (especificacionesAnimal != null) {

            especificacionesAnimal.dispose();
        }

        especificacionesAnimal = new EspecificacionesAnimal(parent);
        especificacionesAnimal.setConsulta(true);

        if (!animal_detalles.id_animal.equals("")) {

            especificacionesAnimal.setId_animal(animal_detalles.id_animal);
        }

        especificacionesAnimal.setVisible(true);
    }//GEN-LAST:event_btn_detallesActionPerformed

    private void visualizarDatos() {

        tipo = 1;
        fecha_ini = new Date();
        fecha_fin = new Date();
        animal = new Animal();

        if (this.jrb_fecha.isSelected()) {

            tipo = 1;
            fecha_ini = calendar1.getDate();
        } else if (this.jrb_entre_fechas.isSelected()) {

            tipo = 2;
            fecha_ini = calendar2.getDate();
            fecha_fin = calendar3.getDate();
        } else if (this.jrb_tagId.isSelected()) {

            tipo = 3;
            animal.cargarPorAreteVisual(this.tagIdSelector.getSelectedItem().toString(),"V");
        } else if (this.jrb_EID.isSelected()) {

            tipo = 4;
            animal.cargarPorAreteElectronicoStatus(this.EIDSelector.getSelectedItem().toString(),"V");
            fecha_ini = calendar4.getDate();
        }

        this.cargarSalidas();
    }

    private void validarRadioButton() {

        this.calendar1.setEnabled(false);
        this.calendar2.setEnabled(false);
        this.calendar3.setEnabled(false);
        this.tagIdSelector.setEnabled(false);
        this.EIDSelector.setEnabled(false);
        this.calendar4.setEnabled(false);

        if (this.jrb_fecha.isSelected()) {

            this.calendar1.setEnabled(true);
        }
        if (this.jrb_entre_fechas.isSelected()) {
            this.calendar2.setEnabled(true);
            this.calendar3.setEnabled(true);
        }
        if (this.jrb_tagId.isSelected()) {
            this.tagIdSelector.setEnabled(true);
        }
        if (this.jrb_EID.isSelected()) {
         
            this.EIDSelector.setEnabled(true);
            this.calendar4.setEnabled(true);
        }
    }
    
